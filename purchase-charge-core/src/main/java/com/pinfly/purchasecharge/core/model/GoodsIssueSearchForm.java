package com.pinfly.purchasecharge.core.model;

public class GoodsIssueSearchForm extends SearchForm
{
    private static final long serialVersionUID = 1L;

    long goodsId;
    String goodsSerialNumber;
    GoodsIssueStatusCode issueStatusCode;
    long customerId;
    long providerId;
    long signUserId;

    public long getGoodsId ()
    {
        return goodsId;
    }

    public void setGoodsId (long goodsId)
    {
        this.goodsId = goodsId;
    }

    public String getGoodsSerialNumber ()
    {
        return goodsSerialNumber;
    }

    public void setGoodsSerialNumber (String goodsSerialNumber)
    {
        this.goodsSerialNumber = goodsSerialNumber;
    }

    public GoodsIssueStatusCode getIssueStatusCode ()
    {
        return issueStatusCode;
    }

    public void setIssueStatusCode (GoodsIssueStatusCode issueStatusCode)
    {
        this.issueStatusCode = issueStatusCode;
    }

    public long getCustomerId ()
    {
        return customerId;
    }

    public void setCustomerId (long customerId)
    {
        this.customerId = customerId;
    }

    public long getProviderId ()
    {
        return providerId;
    }

    public void setProviderId (long providerId)
    {
        this.providerId = providerId;
    }

    public long getSignUserId ()
    {
        return signUserId;
    }

    public void setSignUserId (long signUserId)
    {
        this.signUserId = signUserId;
    }
}
