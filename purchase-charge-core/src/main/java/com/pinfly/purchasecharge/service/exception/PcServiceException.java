package com.pinfly.purchasecharge.service.exception;

public class PcServiceException extends RuntimeException
{
    private static final long serialVersionUID = 1L;

    public PcServiceException ()
    {
        super ();
    }

    public PcServiceException (String message)
    {
        super (message);
    }

    public PcServiceException (Throwable cause)
    {
        super (cause);
    }

    public PcServiceException (String message, Throwable cause)
    {
        super (message, cause);
    }
}
