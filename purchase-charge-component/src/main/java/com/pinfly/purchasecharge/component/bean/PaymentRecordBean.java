package com.pinfly.purchasecharge.component.bean;

import javax.validation.constraints.NotNull;

public class PaymentRecordBean extends BaseBean
{
    private static final long serialVersionUID = 1L;

    private float paid;
    private String comment;
    @NotNull
    private PaymentBean paymentBean;

    private PaymentAccountBean paymentAccountBean;
    private PaymentWayBean paymentWayBean;
    private OrderBean orderBean;

    public float getPaid ()
    {
        return paid;
    }

    public void setPaid (float paid)
    {
        this.paid = paid;
    }

    public String getComment ()
    {
        return comment;
    }

    public void setComment (String comment)
    {
        this.comment = comment;
    }

    public PaymentBean getPaymentBean ()
    {
        return paymentBean;
    }

    public void setPaymentBean (PaymentBean paymentBean)
    {
        this.paymentBean = paymentBean;
    }

    public PaymentAccountBean getPaymentAccountBean ()
    {
        return paymentAccountBean;
    }

    public void setPaymentAccountBean (PaymentAccountBean paymentAccountBean)
    {
        this.paymentAccountBean = paymentAccountBean;
    }

    public PaymentWayBean getPaymentWayBean ()
    {
        return paymentWayBean;
    }

    public void setPaymentWayBean (PaymentWayBean paymentWayBean)
    {
        this.paymentWayBean = paymentWayBean;
    }

    public OrderBean getOrderBean ()
    {
        return orderBean;
    }

    public void setOrderBean (OrderBean orderBean)
    {
        this.orderBean = orderBean;
    }
}
